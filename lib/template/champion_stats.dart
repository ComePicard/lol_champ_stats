class ChampionStats {
  int hp;
  int hpLvl;
  int mana;
  int manaLvl;
  int movementSpeed;
  int armor;
  double armorLvl;
  int magicResistance;
  double magicResistanceLvl;
  int ad;
  int adLvl;
  double attackSpeed;
  double attackSpeedLvl;
  int attackRange;

  ChampionStats({
    required this.hp,
    required this.hpLvl,
    required this.mana,
    required this.manaLvl,
    required this.movementSpeed,
    required this.armor,
    required this.armorLvl,
    required this.magicResistance,
    required this.magicResistanceLvl,
    required this.ad,
    required this.adLvl,
    required this.attackSpeed,
    required this.attackSpeedLvl,
    required this.attackRange,
  });
}
